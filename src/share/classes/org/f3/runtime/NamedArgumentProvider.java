package org.f3.runtime;

/**
 * Defines a mechanism for libraries to provide access to named
 * arguments. This is needed to allow F3 programs embedded
 * in applets to access the incoming applet parameters in a portable
 * manner.
 */
public interface NamedArgumentProvider {

    /**
     * Returns the given named argument, or null if the given argument
     * is not present. This usually returns a String, but some
     * environments may return some other kinds of values.
     */
    public Object get(String name);
}
